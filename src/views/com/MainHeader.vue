        } else {
          this.$store.commit('TOGGLE_SIDEBAR', 'collapse')
        }
      } else {
        if (this.$store.state.cssTheme.sidebar === 'mini') {
          this.$store.commit('TOGGLE_SIDEBAR', '')
        } else {
          this.$store.commit('TOGGLE_SIDEBAR', 'mini')
        }
